    private void BntGuardarRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntGuardarRegistrarActionPerformed
        if (!this.TFNombreRegistrar.getText().equals("") && !this.TFCantidadRegistrar.getText().equals("") && !this.TFPrecioRegistrar.getText().equals("")) {
            String nombre = this.TFNombreRegistrar.getText();
            int cantidad = Integer.valueOf(this.TFCantidadRegistrar.getText());
            double precio = Double.valueOf(this.TFPrecioRegistrar.getText());
            String categoria = this.CBCategoriaRegistrar.getSelectedItem().toString();

            try {
                Controller.registrarProducto(nombre, cantidad, categoria, precio);
                JOptionPane.showMessageDialog(null, "Registro guardado con éxito", "Nuevo Registro", JOptionPane.PLAIN_MESSAGE);
                this.TFNombreRegistrar.setText("");
                this.TFCantidadRegistrar.setText("");
                this.TFPrecioRegistrar.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "No se pudo guardar el registro", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Ingrese valores validos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BntGuardarRegistrarActionPerformed

    private void BntActualizarConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntActualizarConsultarActionPerformed
        String idString = this.TFIdConsultar.getText();
        String nombre = this.TFNombreConsultar.getText();
        String cantidad = this.TFCantidadConsultar.getText();
        String categoria = this.CBCategoriaConsultar.getSelectedItem().toString();
        String precio = this.TFPrecioConsultar.getText();

        if (idString.length() == 0 || nombre.length() == 0 || cantidad.length() == 0 || precio.length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese valores validos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean existe = false;
            List<Producto> listaProductos = Controller.listarProductos();
            int id = Integer.valueOf(idString);
            for (int i = 0; i < listaProductos.size(); i++) {
                if (listaProductos.get(i).getId() == id) {
                    existe = true;
                    break;
                }
            }
            if (existe) {
                Controller.actualizarProducto(id, nombre, Integer.valueOf(cantidad), categoria, Double.valueOf(precio));
                JOptionPane.showMessageDialog(null, "Se ha actualizado el registro", "Actualizar", JOptionPane.PLAIN_MESSAGE);
                this.TFIdConsultar.setText("");
                this.TFNombreConsultar.setText("");
                this.TFCantidadConsultar.setText("");
                this.TFPrecioConsultar.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "El id ingresado no existe", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BntActualizarConsultarActionPerformed

    private void BntConsultarConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntConsultarConsultarActionPerformed
        String idString = this.TFIdConsultar.getText();
        DefaultTableModel modelo = (DefaultTableModel) this.TablaConsultar.getModel();
        modelo.setRowCount(0);
        List<Producto> listaProductos = Controller.listarProductos();

        if (idString.length() == 0) {
            for (int i = 0; i < listaProductos.size(); i++) {
                Producto p1 = listaProductos.get(i);
                modelo.insertRow(i, new Object[]{p1.getId(), p1.getNombre(), p1.getCategoria(), p1.getCantidad(), p1.getPrecio()});
            }
        } else {
            boolean existe = false;
            int id = Integer.valueOf(idString);

            for (int i = 0; i < listaProductos.size(); i++) {
                if (listaProductos.get(i).getId() == id) {
                    existe = true;
                    break;
                }
            }
            if (existe) {
                Producto p2 = Controller.obtenerProducto(id);
                modelo.insertRow(0, new Object[]{p2.getId(), p2.getNombre(), p2.getCategoria(), p2.getCantidad(), p2.getPrecio()});
            } else {
                JOptionPane.showMessageDialog(null, "El id ingresado no existe", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BntConsultarConsultarActionPerformed

    private void BntEliminarConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntEliminarConsultarActionPerformed
        String idString = this.TFIdConsultar.getText();

        if (idString.length() == 0) {
            JOptionPane.showMessageDialog(null, "No ha ingresado un id", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean existe = false;
            List<Producto> listaProductos = Controller.listarProductos();
            int id = Integer.valueOf(idString);
            for (int i = 0; i < listaProductos.size(); i++) {
                if (listaProductos.get(i).getId() == id) {
                    existe = true;
                    break;
                }
            }
            if (existe) {
                Controller.borrarProducto(id);
                JOptionPane.showMessageDialog(null, "Se ha eliminado el registro", "Eliminar", JOptionPane.PLAIN_MESSAGE);
                this.TFIdConsultar.setText("");
                this.TFNombreConsultar.setText("");
                this.TFCantidadConsultar.setText("");
                this.TFPrecioConsultar.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "El id ingresado no existe", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BntEliminarConsultarActionPerformed

