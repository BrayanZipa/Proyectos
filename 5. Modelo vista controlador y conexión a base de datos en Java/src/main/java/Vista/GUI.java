    private void rellenarTabla() {

        int fila = this.TablaConsultar.getSelectedRow();

        if (this.TablaConsultar.getValueAt(fila, 0) == null) {
            JOptionPane.showMessageDialog(null, "No hay datos, presione consultar", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String id = String.valueOf(this.TablaConsultar.getValueAt(fila, 0));
            String nombre = String.valueOf(this.TablaConsultar.getValueAt(fila, 1));
            String categoria = String.valueOf(this.TablaConsultar.getValueAt(fila, 2));
            String cantidad = String.valueOf(this.TablaConsultar.getValueAt(fila, 3));
            String precio = String.valueOf(this.TablaConsultar.getValueAt(fila, 4));
            
            this.TFIdConsultar.setText(id);
            this.TFNombreConsultar.setText(nombre);
            this.TFCantidadConsultar.setText(cantidad);
            this.TFPrecioConsultar.setText(precio);
            this.CBCategoriaConsultar.setSelectedItem(categoria);
        }
    }

